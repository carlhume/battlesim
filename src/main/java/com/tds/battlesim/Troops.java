package com.tds.battlesim;

public class Troops {

	private String name;
	private int count;
	
	public Troops() {}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
